// A. Prison Break
// https://codeforces.com/problemset/problem/1482/A
// Time 15 ms
// Memory 0 KB
// Rating 800

#include <bits/stdc++.h>

using namespace std;

int main() {
  ios_base::sync_with_stdio(false);
  cin.tie(NULL);
  int t;
  cin >> t;
  while (t--)
    {int a, b;
  cin >> a >> b;
  cout << a * b << "\n";}

  return 0;
}